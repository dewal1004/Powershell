function Get-NAVClientUserSettings
{
    [CmdletBinding()]
    param
        (
            [string]$ConfigurationName = ""
        )
    PROCESS
    {
        $clientUserSettings = Join-Path $env:ProgramData "Microsoft\Microsoft Dynamics NAV\140\ClientUserSettings${ConfigurationName}.config"

        [xml] $content = Get-Content $clientUserSettings
        if (!$content)
        {
            Write-Error "Could not load the content of the '$clientUserSettings' file."
        }

        return $content
    }
}
